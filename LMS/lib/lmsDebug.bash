#	Global declarations
#
# ******************************************************************************

# ******************************************************************************
# ******************************************************************************
#
#						Functions
#
# ******************************************************************************
# ******************************************************************************

# ******************************************************************************
#
#	lmsDebugResetScreen
#
#		reset the screen to defaults
#
#	parameters:
#		none
#
#	returns:
#		0 => no error
#
# ******************************************************************************
lmsDebugResetScreen()
{
	echo "${lmsclr_NoColor}"
}

# ******************************************************************************
#
#	lmsDebugFuncCommand
#
#		reset the screen to defaults
#
#	parameters:
#		none
#
#	returns:
#		0 => no error
#
# ******************************************************************************
lmsDebugFuncCommand()
{
	trap lmsDebugResetScreen INT
	/bin/bash $FLAGS $SCRIPT
}

